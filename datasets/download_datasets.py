#!/usr/bin/env python3
"""
Download all datasets needed for the Lying Style NLP research project.

Research hypothesis: "The distribution of text generated by a language model
when instructed to lie is noticeably different from the distribution of text
the model produces under normal, truthful conditions."

Usage:
    python datasets/download_datasets.py

Requirements:
    pip install datasets requests
"""

import io
import json
import os
import zipfile

import requests
from datasets import load_dataset


DATASETS_DIR = os.path.dirname(os.path.abspath(__file__))


def download_azaria_true_false():
    """
    Download Azaria & Mitchell's true-false dataset.

    Source: azariaa.com/Content/Datasets/true-false-dataset.zip
    Paper: "The Internal State of an LLM Knows When It's Lying" (Azaria & Mitchell, 2023)

    Contains true/false statements across 6 topics: animals, capitals, cities,
    companies, elements, inventions. Each file has statements labeled 1 (true)
    or 0 (false).

    Relevance: Can be used as prompts to instruct LLMs to affirm or deny
    known facts, enabling comparison of truthful vs deceptive outputs.
    """
    save_path = os.path.join(DATASETS_DIR, "azaria_true_false")
    if os.path.exists(save_path) and os.listdir(save_path):
        print(f"  [skip] Already exists: {save_path}")
        return

    url = "https://azariaa.com/Content/Datasets/true-false-dataset.zip"
    print(f"  Downloading from {url}...")
    r = requests.get(url, timeout=60)
    r.raise_for_status()
    os.makedirs(save_path, exist_ok=True)
    with zipfile.ZipFile(io.BytesIO(r.content)) as z:
        z.extractall(save_path)
    print(f"  Saved to {save_path}")


def download_truthfulqa():
    """
    Download TruthfulQA dataset (both generation and multiple_choice configs).

    Source: HuggingFace truthful_qa
    Paper: "TruthfulQA: Measuring How Models Mimic Human Falsehoods" (Lin et al., 2022)

    817 questions spanning 38 categories. Each question has correct and
    incorrect reference answers.

    Relevance: Provides questions where LLMs are known to produce false answers,
    enabling study of truthful vs untruthful generation distributions.
    """
    # Generation config
    gen_path = os.path.join(DATASETS_DIR, "truthfulqa")
    if not os.path.exists(gen_path):
        print("  Downloading TruthfulQA (generation)...")
        ds = load_dataset("truthful_qa", "generation")
        os.makedirs(gen_path, exist_ok=True)
        ds.save_to_disk(gen_path)
        print(f"  Saved to {gen_path} ({len(ds['validation'])} examples)")
    else:
        print(f"  [skip] Already exists: {gen_path}")

    # Multiple choice config
    mc_path = os.path.join(DATASETS_DIR, "truthfulqa_mc")
    if not os.path.exists(mc_path):
        print("  Downloading TruthfulQA (multiple_choice)...")
        ds = load_dataset("truthful_qa", "multiple_choice")
        os.makedirs(mc_path, exist_ok=True)
        ds.save_to_disk(mc_path)
        print(f"  Saved to {mc_path} ({len(ds['validation'])} examples)")
    else:
        print(f"  [skip] Already exists: {mc_path}")


def download_counterfact():
    """
    Download CounterFact dataset (NeelNanda/counterfact-tracing).

    Paper: "Locating and Editing Factual Associations in GPT" (Meng et al., 2022)

    21,919 factual statements with true and false completions (e.g.,
    "The mother tongue of Danielle Darrieux is" -> true: "French", false: "English").

    Relevance: Provides paired true/false factual completions, ideal for
    comparing LLM internal representations when generating true vs false statements.
    """
    save_path = os.path.join(DATASETS_DIR, "counterfact")
    if os.path.exists(save_path):
        print(f"  [skip] Already exists: {save_path}")
        return

    print("  Downloading CounterFact...")
    ds = load_dataset("NeelNanda/counterfact-tracing")
    os.makedirs(save_path, exist_ok=True)
    ds.save_to_disk(save_path)
    print(f"  Saved to {save_path} ({len(ds['train'])} examples)")


def download_fever_nli():
    """
    Download FEVER NLI dataset (pietrolesci/nli_fever).

    Paper: "FEVER: a Large-scale Dataset for Fact Extraction and VERification"
           (Thorne et al., 2018)

    248,342 claim-evidence pairs labeled as SUPPORTS, REFUTES, or NOT ENOUGH INFO.

    Relevance: Provides verified true/false claims with evidence, useful for
    constructing prompts where LLMs must generate text supporting or refuting
    known facts.
    """
    save_path = os.path.join(DATASETS_DIR, "fever_nli")
    if os.path.exists(save_path):
        print(f"  [skip] Already exists: {save_path}")
        return

    print("  Downloading FEVER NLI...")
    ds = load_dataset("pietrolesci/nli_fever")
    os.makedirs(save_path, exist_ok=True)
    ds.save_to_disk(save_path)
    print(f"  Saved to {save_path}")
    for split in ds:
        print(f"    {split}: {len(ds[split])} examples")


def download_liar():
    """
    Download LIAR dataset (UKPLab/liar).

    Paper: "Liar, Liar Pants on Fire: A New Benchmark Dataset for Fake Statement
           Detection" (Wang, 2017)

    12,836 short statements from PolitiFact labeled with 6 fine-grained labels:
    pants-fire, false, barely-true, half-true, mostly-true, true.

    Relevance: Real-world graded truthfulness labels on human statements,
    useful as a benchmark for deception detection and as prompts for LLM
    truth/lie generation experiments.
    """
    save_path = os.path.join(DATASETS_DIR, "liar")
    if os.path.exists(save_path):
        print(f"  [skip] Already exists: {save_path}")
        return

    print("  Downloading LIAR...")
    ds = load_dataset("UKPLab/liar")
    os.makedirs(save_path, exist_ok=True)
    ds.save_to_disk(save_path)
    print(f"  Saved to {save_path}")
    for split in ds:
        print(f"    {split}: {len(ds[split])} examples")


def create_samples():
    """Create small JSON sample files for quick inspection."""
    from datasets import load_from_disk

    samples_dir = os.path.join(DATASETS_DIR, "samples")
    os.makedirs(samples_dir, exist_ok=True)

    dataset_configs = [
        ("truthfulqa", "validation", "truthfulqa_sample.json"),
        ("liar", "train", "liar_sample.json"),
        ("counterfact", "train", "counterfact_sample.json"),
        ("fever_nli", "train", "fever_nli_sample.json"),
    ]

    for ds_name, split, filename in dataset_configs:
        out_path = os.path.join(samples_dir, filename)
        ds_path = os.path.join(DATASETS_DIR, ds_name)
        if not os.path.exists(ds_path):
            print(f"  [skip] {ds_name} not downloaded yet")
            continue
        if os.path.exists(out_path):
            print(f"  [skip] {out_path} already exists")
            continue
        ds = load_from_disk(ds_path)
        sample = [ds[split][i] for i in range(min(10, len(ds[split])))]
        with open(out_path, "w") as f:
            json.dump(sample, f, indent=2)
        print(f"  Created {out_path} (10 samples)")


def main():
    print("=" * 60)
    print("Downloading datasets for Lying Style NLP research")
    print("=" * 60)

    print("\n1. Azaria & Mitchell True-False Dataset")
    try:
        download_azaria_true_false()
    except Exception as e:
        print(f"  [FAIL] Could not download: {e}")
        print("  Manual download: https://azariaa.com/Content/Datasets/true-false-dataset.zip")
        print("  Extract to: datasets/azaria_true_false/")

    print("\n2. TruthfulQA")
    download_truthfulqa()

    print("\n3. CounterFact")
    download_counterfact()

    print("\n4. FEVER NLI")
    download_fever_nli()

    print("\n5. LIAR")
    download_liar()

    print("\n6. Creating sample files")
    create_samples()

    print("\n" + "=" * 60)
    print("Dataset download complete!")
    print("=" * 60)


if __name__ == "__main__":
    main()
